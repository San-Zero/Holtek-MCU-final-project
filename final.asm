include HT66F70A.inc

ds		.section		'data'
DEL1		DB		?
DEL2		DB		?
DEL3		DB		?
DC_FLAG		DBIT		
LINE_COUNT	DB		?
DATA		DB		?

STACK_A		DB		?
STACK_PSW	DB		?
STACK_DEL1 	DB		?
STACK_DEL2 	DB		?
STACK_DEL3 	DB		?
STACK_LINE_COUNT	DB	?
STACK_DATA	DB		?

STACK_TBHP	DB		?
STACK_TBLP	DB		?

DELA 	DB ?
PITCH	DB ?		
COUNT1 	DB ?
COUNT2 	DB ?


LCM_EN		EQU		PE.2
LCM_RW		EQU		PE.1
LCM_RS		EQU		PE.0
LCM_ENC		EQU		PEC.2
LCM_RWC		EQU		PEC.1
LCM_RSC		EQU		PEC.0
LCM_DATAPORT	EQU	PG
LCM_DATAPORTC	EQU	PGC

ROMBANK	0 cs

cs		.section	at	00h		'code'
	ORG		00H
	JMP		INIT
	ORG		04H
	JMP		ISR_EXTINT0	
	ORG		08H
	JMP		ISR_EXTINT1	
INIT:	
	MOV		A,10101111B
	MOV		WDTC,A
	CALL	INIT_LCM
	
	MOV		A,10101010B
	MOV		INTEG,A
	
	SET		PAPU.3
	SET		PAC.3
	SET		INT0E
	
	SET		PAPU.4
	SET		PAC.4
	SET		INT1E
	
	SET		PCPU.4
	SET		PCC.4
	SET		INT2E
	
	SET		PCPU.5
	SET		PCC.5
	SET		INT3E
	SET		EMI

MAIN:	
	MOV		A,HIGH TAB_DATA
	MOV		TBHP,A
	MOV		A,LOW TAB_DATA
	MOV		TBLP,A
	MOV		A,01H			;清除顯示區
	CALL	WLCMC
	CALL	DELAY
	MOV		A,80H			;設定顯示第一列，位置0
	CALL	WLCMC
	CALL	DELAY
	MOV		A,0BH
	MOV		LINE_COUNT,A	;設定每列字數
MAIN_1:
	TABRD	DATA
	MOV		A,DATA			;寫入顯示資料
	CALL	WLCMD
	CALL	DELAY
	INC		TBLP
	SDZ		LINE_COUNT		;可顯示字數是否為0?
	JMP		MAIN_1			;不是0，繼續顯示下個字
	MOV		A,0C0H			;設定顯示第二列，位置0
	CALL	WLCMC
	CALL	DELAY
	MOV		A,0AH
	MOV		LINE_COUNT,A	;設定每列字數
MAIN_2:
	TABRD	DATA
	MOV		A,DATA			;寫入顯示資料
	CALL	WLCMD
	CALL	DELAY
	INC		TBLP
	SDZ		LINE_COUNT		;可顯示字數是否為0?
	JMP		MAIN_2			;不是0，繼續顯示下個字
	JMP		MAIN			;重新顯示

;===========================================
;	中斷發生，撥放第1首歌曲
;===========================================
ISR_EXTINT0:
	MOV		STACK_A,A		;累加器資料暫存
	MOV		A,STATUS		
	MOV		STACK_PSW,A		;MCU狀態暫存
	MOV		A,DEL1			;DEL1變數暫存
	MOV		STACK_DEL1,A
	MOV		A,DEL2			;DEL2變數暫存
	MOV		STACK_DEL2,A
	MOV		A,DEL3			;DEL3變數暫存
	MOV		STACK_DEL3,A
	MOV		A,TBHP
	MOV		STACK_TBHP,A
	MOV		A,TBLP
	MOV		STACK_TBLP,A
	
	CLR 	PCC.2
	MOV		A,HIGH TAB_PITCH_A
	MOV		TBHP,A
	MOV		A,LOW TAB_PITCH_A
	MOV		TBLP,A
NEXT_PITCH_A:
	TABRD	PITCH
	CLR		ACC
	XOR		A,TBLH
	SZ		Z
	JMP		END_INT0
	MOV		COUNT1,A
	INC		TBLP
LOOP_A:
	MOV		A,10
	MOV     COUNT2,A
H1_A:
	SET 	PC.2
	MOV 	A,PITCH
	CALL 	DELAY_BEEP
	CLR		PC.2
	MOV		A,PITCH
	CALL	DELAY_BEEP
	SDZ		COUNT2
	JMP		H1_A
	SDZ		COUNT1
	JMP		LOOP_A
	MOV 	A,30
	MOV		COUNT1,A
STOP_A:
	CALL	DELAY_BEEP
	SDZ		COUNT1
	JMP 	STOP_A
	JMP		NEXT_PITCH_A
END_INT0:
	MOV		A,STACK_TBHP
	MOV		TBHP,A
	MOV		A,STACK_TBLP
	MOV		TBLP,A
	MOV		A,STACK_DEL3	;DEL3變數回復
	MOV		DEL3,A
	MOV		A,STACK_DEL2	;DEL2變數回復
	MOV		DEL2,A
	MOV		A,STACK_DEL1	;DEL1變數回復
	MOV		DEL1,A
	MOV		A,STACK_PSW		;MCU狀態回復
	MOV		STATUS,A
	MOV		A,STACK_A		;累加器資料回復
	CLR		INT0F
	RETI
;===========================================
;	中斷發生，撥放第2首歌曲
;===========================================
ISR_EXTINT1:
	MOV		STACK_A,A		;累加器資料暫存
	MOV		A,STATUS		
	MOV		STACK_PSW,A		;MCU狀態暫存
	MOV		A,DEL1			;DEL1變數暫存
	MOV		STACK_DEL1,A
	MOV		A,DEL2			;DEL2變數暫存
	MOV		STACK_DEL2,A
	MOV		A,DEL3			;DEL3變數暫存
	MOV		STACK_DEL3,A
	MOV		A,TBHP
	MOV		STACK_TBHP,A
	MOV		A,TBLP
	MOV		STACK_TBLP,A

	CLR 	PCC.2
	MOV		A,HIGH TAB_PITCH_B
	MOV		TBHP,A
	MOV		A,LOW TAB_PITCH_B
	MOV		TBLP,A
NEXT_PITCH_B:
	TABRD	PITCH
	CLR		ACC
	XOR		A,TBLH
	SZ		Z
	JMP		END_INT1
	MOV		COUNT1,A
	INC		TBLP
LOOP_B:
	MOV		A,10
	MOV     COUNT2,A
H1_B:
	SET 	PC.2
	MOV 	A,PITCH
	CALL 	DELAY_BEEP
	CLR		PC.2
	MOV		A,PITCH
	CALL	DELAY_BEEP
	SDZ		COUNT2
	JMP		H1_B
	SDZ		COUNT1
	JMP		LOOP_B
	MOV 	A,30
	MOV		COUNT1,A
STOP_B:
	CALL	DELAY_BEEP
	SDZ		COUNT1
	JMP 	STOP_B
	JMP		NEXT_PITCH_B
END_INT1:
	MOV		A,STACK_TBHP
	MOV		TBHP,A
	MOV		A,STACK_TBLP
	MOV		TBLP,A
	MOV		A,STACK_DEL3	;DEL3變數回復
	MOV		DEL3,A
	MOV		A,STACK_DEL2	;DEL2變數回復
	MOV		DEL2,A
	MOV		A,STACK_DEL1	;DEL1變數回復
	MOV		DEL1,A
	MOV		A,STACK_PSW		;MCU狀態回復
	MOV		STATUS,A
	MOV		A,STACK_A		;累加器資料回復
	CLR		INT1F
	RETI
	
INIT_LCM	PROC
	CLR		LCM_EN
	CLR		LCM_RW
	CLR		LCM_RS
	CLR		LCM_ENC
	CLR		LCM_RWC
	CLR		LCM_RSC
	CALL	DELAY
	MOV		A,38H
	CALL	WLCMC
	MOV		A,0FH
	CALL	WLCMC
	MOV		A,06H
	CALL	WLCMC
	MOV		A,01H
	CALL	WLCMC
	RET
INIT_LCM 	ENDP

WLCMD		PROC
	SET		DC_FLAG
	JMP		$+3
WLCMC:	
	CLR		DC_FLAG
	SET		LCM_DATAPORTC
	CLR		LCM_RS
	SET		LCM_RW
	NOP
	CALL	WAIT
	SET		LCM_EN
	NOP
	CALL	WAIT
WF:	
	SZ		LCM_DATAPORT.7
;	JMP		WF
	CLR		LCM_EN
	CLR		LCM_DATAPORTC
	MOV		LCM_DATAPORT,A
	CLR		LCM_RW
	CLR		LCM_RS
	SZ		DC_FLAG
	SET		LCM_RS
	SET		LCM_EN
	NOP
	CALL	WAIT
	CLR		LCM_EN
	RET
WLCMD		ENDP

DELAY_BEEP 	PROC
		MOV		DELA,A
DEL_A:	NOP
		NOP
		NOP
		NOP
		NOP
		NOP
		NOP
		SDZ		DELA
		JMP		DEL_A
		RET
DELAY_BEEP ENDP

DELAY   	PROC
	MOV		A,2
    MOV     DEL1,A
DEL_1:  
	MOV     A,30
    MOV     DEL2,A
DEL_2:
    MOV     A,110
    MOV     DEL3,A
DEL_3:
    SDZ     DEL3
    JMP     DEL_3
    SDZ     DEL2
    JMP     DEL_2
    SDZ     DEL1
    JMP     DEL_1
    RET
DELAY   	ENDP
WAIT		PROC
	NOP
	NOP
	NOP
	NOP
	NOP
	NOP
	RET
WAIT		ENDP

