WLCMD		PROC
	SET		DC_FLAG
	JMP		$+3
WLCMC:	
	CLR		DC_FLAG
	SET		LCM_DATAPORTC
	CLR		LCM_RS
	SET		LCM_RW
	NOP
	CALL	WAIT
	SET		LCM_EN
	NOP
	CALL	WAIT
WF:	
	SZ		LCM_DATAPORT.7
;	JMP		WF
	CLR		LCM_EN
	CLR		LCM_DATAPORTC
	MOV		LCM_DATAPORT,A
	CLR		LCM_RW
	CLR		LCM_RS
	SZ		DC_FLAG
	SET		LCM_RS
	SET		LCM_EN
	NOP
	CALL	WAIT
	CLR		LCM_EN
	RET
WLCMD		ENDP

